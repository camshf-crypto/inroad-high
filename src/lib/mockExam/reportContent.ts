/**
 * 회차별 시기별 가이드
 */
export function getSeasonGuide(grade: string, period: string): SeasonGuide {
  const key = `${grade}_${period}`
  return REPORT_CONTENT[key]?.season_guide ?? FALLBACK.season_guide
}

/**
 * 회차별 생기부 방향성 (학과명은 동아리 항목에 자동 삽입)
 */
export function getSaenggibuDirection(
  grade: string,
  period: string,
  department: string
): SaenggibuDirection {
  const key = `${grade}_${period}`
  const base = REPORT_CONTENT[key]?.saenggibu_direction ?? FALLBACK.saenggibu_direction

  // 동아리 첫 항목에 학과명 삽입
  if (department) {
    return {
      ...base,
      동아리: base.동아리.map((item, idx) =>
        idx === 0 ? item.replace(/학과/g, `${department} 학과`) : item
      ),
    }
  }
  return base
}

